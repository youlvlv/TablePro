/// A collection of languages that are supported by `tree-sitter`
public enum TreeSitterLanguage: String {
    case bash
    case html
    case javascript
    case jsdoc
    case json
    case jsx
    case python
    case ruby
    case sql
    case tsx
    case typescript
    case plainText
}
